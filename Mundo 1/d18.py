import math

n = float(input('Digite um número: '))
sen = math.sin(math.radians(n))
cos = math.cos(math.radians(n))
tan = math.tan(math.radians(n))
print('O seno de {} é {:.2f}, o coseno {:.2f} e a tangente {:.2f}!' .format(n, sen, cos, tan))

